import re

def normalize_phone(phone_number: str) -> str:
    """
    Нормалізує телефонний номер до міжнародного стандарту.
    Додає '+38', якщо код країни відсутній.
    """
    # Крок 1: Видаляємо всі символи, крім цифр та '+'
    # .strip() видаляє зайві пробіли на початку та в кінці
    cleaned_number = re.sub(r'[^\d+]', '', phone_number.strip())

    # Крок 2: Стандартизуємо префікс
    if cleaned_number.startswith('+'):
        # Якщо номер вже має будь-який міжнародний код, він вважається правильним
        return cleaned_number
    elif cleaned_number.startswith('380'):
        # Якщо номер починається з 380, додаємо '+'
        return '+' + cleaned_number
    else:
        # В усіх інших випадках вважаємо, що це український номер без коду
        return '+38' + cleaned_number

# --- Приклад використання з різними номерами ---
raw_numbers = [
    "067\t123 4567",         # Український без коду
    "(095) 234-5678\n",       # Український без коду
    "+380 44 123 4567",       # Український з кодом
    "+1 555 123 4567",        # Міжнародний номер іншої країни
    "380501234567",           # Український з кодом, але без '+'
    "+44 20 7123 4567"        # Британський номер
]

sanitized_numbers = [normalize_phone(num) for num in raw_numbers]
print("Нормалізовані номери:", sanitized_numbers)